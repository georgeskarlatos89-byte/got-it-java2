package com.cardinalcommerce.a;

import com.cardinalcommerce.dependencies.internal.nimbusds.jose.JWSAlgorithm;
import java.util.Collections;
import java.util.Set;

public abstract class setImportantForAutofill implements setOnGenericMotionListener {
    private final setNextFocusUpId Cardinal = new setNextFocusUpId();
    private final Set<JWSAlgorithm> configure;

    public setImportantForAutofill(Set<JWSAlgorithm> set) {
        if (set != null) {
            this.configure = Collections.unmodifiableSet(set);
            return;
        }
        throw new IllegalArgumentException("The supported JWS algorithm set must not be null");
    }

    public final Set<JWSAlgorithm> cca_continue() {
        return this.configure;
    }

    public final setNextFocusUpId getInstance() {
        return this.Cardinal;
    }
}
