package com.cardinalcommerce.a;

import java.security.Provider;
import java.security.SecureRandom;

public class setNextFocusUpId {
    public SecureRandom Cardinal;
    public Provider cca_continue;

    public setNextFocusUpId() {
        this((Provider) null, (SecureRandom) null);
    }

    public setNextFocusUpId(Provider provider, SecureRandom secureRandom) {
        this.cca_continue = provider;
        this.Cardinal = secureRandom;
    }
}
